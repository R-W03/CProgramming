#include<stdio.h>

int main(void) {
    int years=18;
    int minutes=0;

    //Write the prompt
    puts("Enter your age in years:");

    //Get the input
    scanf("%d, &age"), years; 

    //Do the math
    years = minutes * 365 * 24 * 60;
    

    //Create the output
    printf("%d years is %d minutes"), years, minutes;

    return 0;
}